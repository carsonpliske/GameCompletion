// Helper function to get achievement data for a game
function getAchievementData(gameName) {
    // Try exact match first
    if (steamAchievements[gameName]) {
        return steamAchievements[gameName];
    }

    // Try some common name variations
    const variations = [
        gameName.replace(/™/g, '').replace(/®/g, '').trim(),
        gameName.replace(/ \(\d{4}\)/g, ''), // Remove year
        gameName.replace(/: Enhanced Edition|: Definitive Edition|: GOTY/g, ''),
        gameName.replace(/Call of Duty®?:? ?/g, 'Call of Duty: '),
        gameName.replace(/Grand Theft Auto V Legacy/g, 'Grand Theft Auto V'),
        gameName.replace(/Alan Wake's/g, 'Alan Wakes') // Handle apostrophe variations
    ];

    for (const variation of variations) {
        if (steamAchievements[variation]) {
            return steamAchievements[variation];
        }
    }

    // Return default (no achievement data available)
    return { hasAchievements: false, count: 0, unknown: true };
}

